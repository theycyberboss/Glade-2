package start;

import org.newdawn.slick.GameContainer;
import org.newdawn.slick.Graphics;
import org.newdawn.slick.state.StateBasedGame;

public abstract class Button {
	
	protected float x,y,width,height;
	
	public Button(float x, float y, float width, float height)
	{
		this.x = x;
		this.y = y;
		this.width = width;
		this.height = height;
	}
	
	public abstract void update(GameContainer gc);
	public abstract void render(Graphics g, GameContainer gc);
	
	
	public abstract void mouseOver(Graphics g, GameContainer gc);
	public abstract void mouseDown(Graphics g, GameContainer gc, StateBasedGame sbg);
	public abstract void mouseUp(Graphics g, GameContainer gc);

	public float getX() {
		return x;
	}

	public void setX(float x) {
		this.x = x;
	}

	public float getY() {
		return y;
	}

	public void setY(float y) {
		this.y = y;
	}

	public float getWidth() {
		return width;
	}

	public void setWidth(float width) {
		this.width = width;
	}

	public float getHeight() {
		return height;
	}

	public void setHeight(float height) {
		this.height = height;
	}

	

}
